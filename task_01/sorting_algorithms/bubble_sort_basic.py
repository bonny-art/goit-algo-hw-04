"""
Модуль для реалізації базового алгоритму сортування бульбашкою.

Алгоритм не має перевірки на раннє завершення і завжди виконує
повну кількість ітерацій, навіть якщо масив уже відсортований.
"""

from typing import List

def bubble_sort_basic(arr: List[int]) -> List[int]:
    """
    Реалізує базовий алгоритм сортування бульбашкою.

    Алгоритм проходить по масиву кілька разів, порівнюючи сусідні елементи і міняючи їх місцями,
    якщо перший більший за другий. Ця версія не має оптимізації на раннє завершення.

    Аргументи:
    - arr (List[int]): Список цілих чисел для сортування.

    Повертає:
    - List[int]: Відсортований список.
    """
    n = len(arr)
    for i in range(n - 1):
        for j in range(0, n - i - 1):
            if arr[j] > arr[j + 1]:
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
    return arr
