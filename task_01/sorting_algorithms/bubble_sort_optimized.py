"""
Модуль для реалізації оптимізованого алгоритму сортування бульбашкою.

Ця версія алгоритму включає перевірку на раннє завершення, що дозволяє завершити сортування раніше,
якщо під час останнього проходу не було жодних перестановок.
"""

from typing import List

def bubble_sort_optimized(arr: List[int]) -> List[int]:
    """
    Реалізує оптимізований алгоритм сортування бульбашкою.

    Алгоритм сортує масив шляхом порівняння сусідніх елементів і їх перестановки.
    Якщо під час одного проходу не відбувається жодної перестановки, алгоритм завершиться раніше.

    Аргументи:
    - arr (List[int]): Список цілих чисел для сортування.

    Повертає:
    - List[int]: Відсортований список.
    """
    n = len(arr)
    for i in range(n):
        swapped = False
        for j in range(0, n - i - 1):
            if arr[j] > arr[j + 1]:
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                swapped = True
        if not swapped:
            break
    return arr
